#include "ft.h"

int		main(int argc, char **argv)
{
	int cnt;

	cnt = 1;
	if (argc == 1)
		return (0);
	else
		while (cnt < argc)
		{
			resolve_all(cnt, argv);
			write(1, "\n", 1);
			cnt++;
		}
	return (0);
}
