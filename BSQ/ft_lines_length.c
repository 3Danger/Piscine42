#include "ft.h"

int		ft_lines_length(char *buf)
{
	int i;
	int size;

	i = 0;
	size = 0;
	while (buf[i] != '\n')
		i++;
	i++;
	while (buf[i] != '\n')
	{
		size++;
		i++;
	}
	return (size);
}
